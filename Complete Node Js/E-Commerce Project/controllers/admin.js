      //     price: price,
      //     description: description,
      //   },
      //   validationErrors: [],
      //   errorMessage: "Sorry! database operation failed, please try agian",
      // });
      // res.redirect("/500");
      const error = new Error(err);
      error.httpStatusCode = 500;
